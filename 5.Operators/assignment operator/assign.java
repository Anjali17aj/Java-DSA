public class assign {
    public static void main(String args[]) {
        int A = 10;
        A = A + 10;
        A += 20; // speeder than a=a+10
        System.out.println(A);

        int b = 20;
        b = b + 20;
        b += 50;
        System.out.println(b);

        int z = 60;
        z = z + 50;
        z *= 20;
        System.out.println(z);

    }
}
