import java.util.*;

public class typeCast {
    public static void main(String args[]) {
        Scanner sc = new Scanner(System.in);
        float a = 17.7879f;
        int b = (int) a;
        System.out.println(b);
        char ch = 'a';
        int ch2 = ch;

        char ch3 = 'b';
        int ch4 = ch3;
        System.out.println(ch4);
    }
}
